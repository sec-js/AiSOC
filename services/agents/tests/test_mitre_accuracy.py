class TestMitreAccuracy(unittest.TestCase):
    """Offline MITRE tactic accuracy evaluation across the synthetic dataset."""

    def test_accuracy_above_threshold(self) -> None:
        result = evaluate_mitre_accuracy()
        print(
            f"\n[eval] MITRE accuracy: {result.correct}/{result.total} = "
            f"{result.accuracy * 100:.1f}% (P={result.precision:.2f} "
            f"R={result.recall:.2f} F1={result.f1:.2f})"
        )
        self.assertGreaterEqual(
            result.accuracy,
            0.80,
            f"MITRE tactic accuracy {result.accuracy:.1%} is below the 80% threshold.\n" + result.to_json()[:4000],
        )

    def test_dataset_minimum_size(self) -> None:
        """The eval dataset must contain at least 200 incidents (Phase-1C target)."""
        self.assertGreaterEqual(
            len(SYNTHETIC_INCIDENTS_DATA),
            200,
            f"Eval dataset has only {len(SYNTHETIC_INCIDENTS_DATA)} incidents; need ≥200 for Phase-1C.",
        )

    def test_each_incident_has_expected_tactics(self) -> None:
        for inc in SYNTHETIC_INCIDENTS_DATA:
            title = inc["title"]
            tactic_ids = inc["expected_tactics"]
            self.assertGreater(
                len(tactic_ids),
                0,
                f"Incident '{title[:60]}' has no expected tactic IDs.",
            )
            for t in tactic_ids:
                self.assertIn(
                    t,
                    _TACTIC_NAMES,
                    f"Unknown tactic ID '{t}' in incident '{title[:60]}'.",
                )

    def test_no_duplicate_incident_ids(self) -> None:
        ids = [inc["id"] for inc in SYNTHETIC_INCIDENTS_DATA]
        self.assertEqual(len(ids), len(set(ids)), "Duplicate incident ids detected.")

    def test_severity_values_valid(self) -> None:
        valid = {"critical", "high", "medium", "low"}
        for inc in SYNTHETIC_INCIDENTS_DATA:
            self.assertIn(inc["severity"], valid, f"Bad severity: {inc['severity']}")

    def test_response_class_values_valid(self) -> None:
        valid = {
            "isolate_host",
            "disable_account",
            "block_indicator",
            "rollback_change",
            "escalate",
            "monitor",
        }
        for inc in SYNTHETIC_INCIDENTS_DATA:
            self.assertIn(
                inc["response_class"],
                valid,
                f"Bad response_class: {inc['response_class']}",
            )

    def test_tactic_coverage(self) -> None:
        """Dataset should cover at least 10 of 12 tracked tactics."""
        all_tactics: set[str] = set()
        for inc in SYNTHETIC_INCIDENTS_DATA:
            all_tactics.update(inc["expected_tactics"])
        self.assertGreaterEqual(
            len(all_tactics & set(_TACTIC_NAMES)),
            10,
            f"Only {len(all_tactics)} tactics covered: {sorted(all_tactics)}",
        )

    def test_per_template_accuracy(self) -> None:
        """Macro-average accuracy across distinct `template_id`s must be ≥80%.

        This is the regression-signal gate: with 200 cases drawn from 55
        templates, a single broken template moves per-case accuracy by only
        ~0.5% (3-4 / 200) but moves per-template accuracy by ~1.8% (1 / 55).
        Equal-weighting templates ensures the substrate cannot regress on a
        whole class of behavior and hide behind the duplicates.
        """
        result = evaluate_mitre_accuracy()
        summary = result.per_template_summary()
        print(
            f"\n[eval] MITRE per-template macro accuracy: "
            f"{summary['template_macro_accuracy'] * 100:.1f}% "
            f"({summary['template_count']} distinct templates, "
            f"{len(summary['failing_templates'])} below 80%)"
        )
        self.assertGreaterEqual(
            summary["template_count"],
            50,
            f"Only {summary['template_count']} distinct templates in dataset; expected ≥50.",
        )
        self.assertGreaterEqual(
            summary["template_macro_accuracy"],
            0.80,
            "Per-template MITRE accuracy below 80%.\n"
            f"Failing templates: {summary['failing_templates']}",
        )

    def test_each_incident_has_template_id(self) -> None:
        """Every incident must carry the `template_id` it was generated from."""
        for inc in SYNTHETIC_INCIDENTS_DATA:
            self.assertIn("template_id", inc, f"Missing template_id on {inc.get('id')}")
            self.assertTrue(
                isinstance(inc["template_id"], str) and inc["template_id"],
                f"Empty template_id on {inc.get('id')}",
            )
            self.assertIn(
                "template_index",
                inc,
                f"Missing template_index on {inc.get('id')}",
            )

    def test_template_distribution_balanced(self) -> None:
        """No single template may account for >5% of the dataset.

        Keeps the per-case headline number from being dominated by any one
        template if the cycle changes.
        """
        counts: dict[str, int] = {}
        for inc in SYNTHETIC_INCIDENTS_DATA:
            tpl = inc["template_id"]
            counts[tpl] = counts.get(tpl, 0) + 1
        worst = max(counts.items(), key=lambda kv: kv[1])
        self.assertLessEqual(
            worst[1],
            max(4, int(len(SYNTHETIC_INCIDENTS_DATA) * 0.05)),
            f"Template '{worst[0]}' appears {worst[1]} times "
            f"(>{int(len(SYNTHETIC_INCIDENTS_DATA) * 0.05)} = 5% of dataset).",
        )


if __name__ == "__main__":
    # Also allow running standalone to print a detailed report
    res = evaluate_mitre_accuracy()
    print(res.to_json())
    passed = res.accuracy >= 0.80
    print(f"\n{'PASS' if passed else 'FAIL'}: {res.accuracy * 100:.1f}% accuracy (threshold: 80%)")
    sys.exit(0 if passed else 1)
